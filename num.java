public class num {
    public static void main(String[] args) {

        int num = 12;

        System.out.println();
        System.out.println("The Initial Number is " + num);

        num*=2;
        num-=4;
        num++;

        System.out.println();
        System.out.println("The Final Number is " + num);
        System.out.println();
    }
}
